    boolean load() {
        boolean isErrors = false;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        command.setSql( "select '<img title=\"'||decode('{s_DESCRIZIONE}', "
                    + "                               null, "
                    + "                               'Filtro non attivo', "
                    + "                               'Filtro attivo' "
                    + "                             ) "
                    + "          ||'\" alt=\"'||decode('{s_DESCRIZIONE}', "
                    + "                               null, "
                    + "                               'Filtro non attivo', "
                    + "                               'Filtro attivo' "
                    + "                             ) "
                    + "		       	  ||'\" height=\"18\"' "
                    + "           ||' src=\"'||decode('{s_DESCRIZIONE}', "
                    + "                               null, "
                    + "                               '../common/images/AD4/filtro_off.gif', "
                    + "                               '../common/images/AD4/filtro_on.gif') "
                    + "        ||'\" width=\"18\" border=\"0\">' immagine_filtro "
                    + "  from dual" );
        if ( StringUtils.isEmpty( (String) urlS_DESCRIZIONE.getObjectValue() ) ) urlS_DESCRIZIONE.setValue( "" );
        command.addParameter( "s_DESCRIZIONE", urlS_DESCRIZIONE, null );
        if ( ! command.isSetAllParams() ) {
            empty = true;
            ds.closeConnection();
            return true;
        }
        if ( ! StringUtils.isEmpty( orderBy ) ) {
            command.setOrder( orderBy );
        }

        fireBeforeBuildSelectEvent( new DataObjectEvent(command) );


        fireBeforeExecuteSelectEvent( new DataObjectEvent(command) );

        DbRow record = null;
        if ( ! ds.hasErrors() ) {
            record = command.getOneRow();
        }

        CCLogger.getInstance().debug(command.toString());

        fireAfterExecuteSelectEvent( new DataObjectEvent(command) );

        ds.closeConnection();
//End load

//loadDataBind @175-B287E268
        if ( record == null || record.isEmpty() ) {
            empty = true;
        } else {
            row.setIMMAGINE_FILTRO(Utils.convertToString(ds.parse(record.get("IMMAGINE_FILTRO"), row.getIMMAGINE_FILTROField())));
        }
//End loadDataBind

//End of load @175-B1C63002
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        return ( ! isErrors );
    }
//End End of load

