    boolean load() {
        boolean isErrors = false;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        command.setSql( "SELECT *  "
                    + "FROM AD4_UTENTI" );
        if ( ! StringUtils.isEmpty( orderBy ) ) {
            command.setOrder( orderBy );
        }

        fireBeforeBuildSelectEvent( new DataObjectEvent(command) );

        String where1 = WhereParams.rawOperation( "UTENTE", FieldOperation.EQUAL, sesAD4UTENTE, null, ds );
        String whereParams = where1;

        if ( where1 == null ) {
            empty = true;
            ds.closeConnection();
            return true;
        }
        if ( ! StringUtils.isEmpty(whereParams) ) {
            if ( ! StringUtils.isEmpty(command.getWhere()) ) {
                command.setWhere( command.getWhere() + " AND (" + whereParams + ")" );
            } else {
                command.setWhere( whereParams );
            }
        }

        fireBeforeExecuteSelectEvent( new DataObjectEvent(command) );

        DbRow record = null;
        if ( ! ds.hasErrors() ) {
            record = command.getOneRow();
        }

        CCLogger.getInstance().debug(command.toString());

        fireAfterExecuteSelectEvent( new DataObjectEvent(command) );

        ds.closeConnection();
//End load

//loadDataBind @78-8DD0912E
        if ( record == null || record.isEmpty() ) {
            empty = true;
        } else {
            row.setNOMINATIVO(Utils.convertToString(ds.parse(record.get("NOMINATIVO"), row.getNOMINATIVOField())));
        }
//End loadDataBind

//End of load @78-B1C63002
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        return ( ! isErrors );
    }
//End End of load

//checkUnique @78-E37DC8DC
    int checkUnique( VerifiableControl control ) {
        int result = -1;
        boolean isErrors = false;
        if ( StringUtils.isEmpty(control.toString()) ) return 0;
        JDBCConnection ds = JDBCConnectionFactory.getJDBCConnection( "cn" );
        ds.setLocale(page.getCCSLocale().getLocale());
        RawCommand command = new RawCommand( ds );

        String where1 = WhereParams.rawOperation( "UTENTE", FieldOperation.EQUAL, sesAD4UTENTE, null, ds );
        String whereParams = where1;

        command.setCountSql( "SELECT count(*) FROM AD4_UTENTI" );
        where = new StringBuffer( control.getFieldSource() + " = " + ds.toSql( ds.format( control ), control.getType() ) );
        if ( ! StringUtils.isEmpty(whereParams) ) {
            where.append( " AND NOT ( " + whereParams + ")");
        }
        command.setWhere(where.toString());
        result = command.count();
        ds.closeConnection();
        isErrors = ds.hasErrors();
        if ( isErrors ) addErrors( ds.getErrors() );
        if ( isErrors ) result = -1;
        return result;
    }
//End checkUnique

